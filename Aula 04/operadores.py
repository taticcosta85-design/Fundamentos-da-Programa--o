# 1. Operadores Matemáticos 
numero_um = 20
numero_dois = 15

soma = numero_um + numero_dois
print(f"A soma entre {numero_um} + {numero_dois} = {soma}")

subtracao = numero_um - numero_dois
print(f"A subtracao entre {numero_um} - {numero_dois} = {subtracao}")

multiplicacao = numero_um * numero_dois
print(f"A multiplicacao entre {numero_um} * {numero_dois} = {multiplicacao}")

divisao = numero_um / numero_dois
print(f"A divisao entre {numero_um} / {numero_dois} = {divisao}")
